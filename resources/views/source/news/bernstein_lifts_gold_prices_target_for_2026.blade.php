	<h1>Bernstein lifts gold prices target for 2026, cautions upside is limited</h1>

    <div class="block">
		<div class="inner-block">
			<p>Bernstein raised its 2026 gold price target to $4,180 per ounce in a note on Wednesday, warning that several powerful tailwinds may already be largely reflected in the metal’s dramatic multiyear rally. </p>
		</div>
	</div>

	<div class="block">
		<div class="inner-block">
			<p>
				Analyst Bob Brackett said the firm is “marking gold price to market at $4,180/oz in 2026,” citing support from “continued central bank buying … as they diversify reserves away from the US dollar.” 
				He added that “gold ETF inflows accelerated in 2025 … reflecting rising interest from both institutional and retail investors,” while anticipated Federal Reserve rate cuts in 2026 “should also support gold prices.”

				Bernstein highlighted gold’s unusual resilience in the face of rising real rates. The firm noted that real rates rose more than 3% over the past four years while gold surged 135%, calling this “an anomaly, as gold usually has a negative relationship to real rates.” 

				Although the relationship between TIPS and gold appears to have weakened, Bernstein argued it “still exists, although it is no longer linear,” with safe-haven flows, central bank purchases and the “currency debasement trade” driving a structural re-rating since 2023.

				Despite strong underlying demand, Bernstein suggested upside may be constrained. Global central bank purchases slowed in the first nine months of 2025, and while ETF inflows offset the decline, the firm noted that ETF flows are “a pro-cyclical force,” capable of amplifying both rallies and pullbacks.
			</p>
		</div>
	</div>

	<div class="block">
		<div class="inner-block">
			<p>
				Bernstein also warned that the market “has a history of incorrectly predicting Fed rates,” a key variable for gold. 

				Still, the firm reaffirmed an Outperform rating on Barrick, saying it remains its preferred way to express a constructive view on the metal.
			</p>
